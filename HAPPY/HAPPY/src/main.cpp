void clear(){
  for(int i = 0 ; i<8 ; i++){
    digitalWrite(rows[i],LOW);
    digitalWrite(cols[i],HIGH);
  }
}
//显示函数
void Display(unsigned char dat[8][8]){
  for(int c = 0 ; c<8 ; c++){
    digitalWrite(cols[c],LOW);
    for(int r = 0 ; r<8 ; r++){
    digitalWrite(rows[r],dat[r][c]);
  }
  
  }
   delay(800);
   clear();
}

